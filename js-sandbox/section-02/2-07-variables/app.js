console.clear();

// three ways to assign variables
// var, let, const - try to avoid var
var myName = 'Henry Macartney';
console.log(myName);
myName = 'David Macartney';
console.log(myName);
myName = 'Ulli Macartney';
console.log(myName);
myName = 'Vivien Macartney';
console.log(myName);

// same applies for 'let'
let aName = 'Henry Macartney';
console.log(aName);
aName = 'David Macartney';
console.log(aName);
aName = 'Ulli Macartney';
console.log(aName);
aName = 'Vivien Macartney';
console.log(aName);

